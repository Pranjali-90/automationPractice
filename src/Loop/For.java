package Loop;

public class For {
	public static void main(String args[]) {
		for (int i = 1; i < 11; i++) {
			System.out.print(" " + i + " ");
		}
		System.out.println("");
		System.out.println("****reverse****");
		for (int i = 10; i > 0; i--) {
			System.out.println(i);
		}
		int ascivalue = 'A';
		System.out.println("Ascivalue" + ascivalue);

	}

}
